    function badBass(length: number, vol: number) {
        let melod = new music.Melody("~15 @1,0,90,1 !200,90^1");
        melod.play(vol);
    }

    function hiHat(length: number, vol: number) {
        let melod = new music.Melody("@0,50,0,0 ~5 " + "c1-99999 ");
        melod.play(vol);
    }

    // manual stopping since caused issues without
    let oldMelodSawA: music.Melody = null;
    let oldMelodSawB: music.Melody = null;

    function sawMaybe(freq: number, time: number, vol: number) {
        if (oldMelodSawA != null) {
            oldMelodSawA.stop();
            oldMelodSawB.stop();
        }
        let melodA = new music.Melody("~2 @10,1,255,40 !" + freq + "," + time);
        let melodB = new music.Melody("~2 @10,1,255,40 !" + (freq / 2) + "," + time); 

        // alter volumes?
        melodA.play(vol);
        melodB.play(vol);
        
        oldMelodSawA = melodA; 
        oldMelodSawB = melodB;
    }

