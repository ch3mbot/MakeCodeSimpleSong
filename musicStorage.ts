    function acousticgrand (freq: number, length: number, vol: number) {
        music.play(music.createSoundEffect(
        WaveShape.Sine,
        freq,
        freq,
        255 * vol / 255,
        0,
        length,
        SoundExpressionEffect.None,
        InterpolationCurve.Linear
        ), music.PlaybackMode.InBackground)
        music.play(music.createSoundEffect(
        WaveShape.Sine,
        freq * 2,
        freq * 2,
        255 * vol / 255,
        0,
        length,
        SoundExpressionEffect.None,
        InterpolationCurve.Linear
        ), music.PlaybackMode.InBackground)
        music.play(music.createSoundEffect(
        WaveShape.Sine,
        freq * 3,
        freq * 3,
        227 * vol / 255,
        0,
        length,
        SoundExpressionEffect.None,
        InterpolationCurve.Linear
        ), music.PlaybackMode.InBackground)
        music.play(music.createSoundEffect(
        WaveShape.Sine,
        freq * 4,
        freq * 4,
        227 * vol / 255,
        0,
        length,
        SoundExpressionEffect.None,
        InterpolationCurve.Linear
        ), music.PlaybackMode.InBackground)
    }

